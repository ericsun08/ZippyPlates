function openNav() {
    document.getElementById("mySidenav").style.width = "370px";
    document.getElementById("mySidenav").style.boxShadow = "0 4px 8px 8px rgba(0, 0, 0, 0.2)";
}

function closeNav() {
    document.getElementById("mySidenav").style.width = "0";
    document.getElementById("mySidenav").style.boxShadow = "0 0 0 0 rgba(0, 0, 0, 0)";
}